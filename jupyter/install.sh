# it is a classic interactive python web based IDE
pip install notebook

# Launch notebook: After installation, you can start notebook by running:
jupyter notebook

# JupyterLab is a more advanced interface for Jupyter that provides more functionality
# (such as file management, markdown previews, integrated terminals, etc.). You can install JupyterLab if you'd like:
pip install jupyterlab

# Launch JupyterLab: After installation, you can start JupyterLab by running:
jupyter lab

# your browser should automatically open a tab with the Jupyter interface, which usually runs on http://localhost:8888.

